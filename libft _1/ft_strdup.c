#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		size;
	int		i;
	char	*dest;

	size = 0;
	while (str[size])
		size++;
	dest = malloc(sizeof(char) * (size + 1));
	if (!dest)
		return (NULL);
	i = 0;
	while (str[i])
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
